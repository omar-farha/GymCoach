/**
 * Search for exercises using Egyptian Arabic
 * Returns true if the exercise matches the Egyptian search term
 */
export function matchesEgyptianSearch(exercise: any, searchTerm: string): boolean {
  if (!searchTerm) return true

  const lowerSearch = searchTerm.toLowerCase().trim()
  const exerciseName = exercise.name.toLowerCase()
  const exerciseTarget = exercise.target.toLowerCase()
  const exerciseEquipment = exercise.equipment.toLowerCase()
  const exerciseBodyPart = exercise.bodyPart.toLowerCase()

  // Direct English search
  if (
    exerciseName.includes(lowerSearch) ||
    exerciseTarget.includes(lowerSearch) ||
    exerciseEquipment.includes(lowerSearch) ||
    exerciseBodyPart.includes(lowerSearch)
  ) {
    return true
  }

  // Check if search is in Egyptian Arabic - search through all translations
  // Check exercise names
  for (const [english, egyptian] of Object.entries(egyptianExerciseNames)) {
    if (egyptian.includes(lowerSearch)) {
      // Found Egyptian match, now check if exercise contains the English equivalent
      if (
        exerciseName.includes(english.toLowerCase()) ||
        exerciseName.split(' ').some((word: string) => english.toLowerCase().includes(word)) ||
        english.toLowerCase().split(' ').some((word: string) => exerciseName.includes(word))
      ) {
        return true
      }
    }
  }

  // Check body parts
  for (const [english, egyptian] of Object.entries(egyptianBodyParts)) {
    if (egyptian.includes(lowerSearch)) {
      if (
        exerciseBodyPart.includes(english.toLowerCase()) ||
        exerciseTarget.includes(english.toLowerCase()) ||
        exerciseName.includes(english.toLowerCase())
      ) {
        return true
      }
    }
  }

  // Check equipment
  for (const [english, egyptian] of Object.entries(egyptianEquipment)) {
    if (egyptian.includes(lowerSearch)) {
      if (exerciseEquipment.includes(english.toLowerCase())) {
        return true
      }
    }
  }

  // Check target muscles
  for (const [english, egyptian] of Object.entries(egyptianTargetMuscles)) {
    if (egyptian.includes(lowerSearch)) {
      if (
        exerciseTarget.includes(english.toLowerCase()) ||
        exerciseName.includes(english.toLowerCase())
      ) {
        return true
      }
    }
  }

  return false
}
